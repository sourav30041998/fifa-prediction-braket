const combinations: string[] = [];

function collectCombinations(start: number, selected: string[]) {
  if (selected.length === 8) {
    combinations.push(selected.join(""));
    return;
  }
  for (let index = start; index <= alphabet.length - (8 - selected.length); index += 1) {
    collectCombinations(index + 1, [...selected, alphabet[index]]);
  }
}

collectCombinations(0, []);

const allocationByGroups = new Map(
  combinations.map((combination, index) => [
    combination,
    allocations.slice(index * 8, index * 8 + 8)
  ])
);

export const annexCWinners = ["A", "B", "D", "E", "G", "I", "K", "L"] as const;

export function getAnnexCAllocation(qualifyingGroups: string[]) {
  const key = [...qualifyingGroups].sort().join("");
  const allocation = allocationByGroups.get(key);
  if (!allocation) throw new Error(`No FIFA Annex C allocation for groups ${key}`);
  return Object.fromEntries(
    annexCWinners.map((winnerGroup, index) => [winnerGroup, allocation[index]])
  ) as Record<(typeof annexCWinners)[number], string>;
}

export function getAnnexCCombinationCount() {
  return allocationByGroups.size;
}
